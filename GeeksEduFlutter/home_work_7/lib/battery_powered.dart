// Миксин для устройств, работающих от батареи
// Миксин — это способ добавить функциональность без наследования
mixin BatteryPowered {
  // Уровень заряда батареи (по умолчанию 100%)
  int batteryLevel = 100;

  // Вывод текущего уровня заряда
  void showBattery() {
    print('Battery level: $batteryLevel%');
  }
}
